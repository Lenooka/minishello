#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	c = c % 128;
	while (*s != '\0')
	{
		if (*s == (char)c)
		{
			return ((char *)s);
		}
		s++;
	}
	if (c == *s)
		return ((char *)s);
	return (NULL);
}
